CREATE TABLE CONTACT (
    NAME TEXT PRIMARY KEY NOT NULL,
    IS_FAVORITE INTEGER DEFAULT 0
);
CREATE TABLE PHONE (
    PHONE_NUMBER TEXT PRIMARY KEY NOT NULL,
    PHONE_TYPE TEXT NOT NULL,
    NAME TEXT NOT NULL,
    FOREIGN KEY(NAME) REFERENCES CONTACT(NAME)
);



